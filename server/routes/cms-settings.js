'use strict';

const fs     = require('fs');
const router = require('express').Router();
const { CMS_SETTINGS_FILE, NGINX_CONF_FILE, ROOT } = require('../lib/paths');
const { safeError } = require('../lib/validate');

const DEFAULTS = { baseUrl: '' };

function read() {
  try { return { ...DEFAULTS, ...JSON.parse(fs.readFileSync(CMS_SETTINGS_FILE, 'utf-8')) }; }
  catch { return { ...DEFAULTS }; }
}

function buildNginxConf(settings) {
  const baseUrl = settings.baseUrl || '';
  let domain    = 'your-domain.com';
  let cmsDomain = 'cms.your-domain.com';

  if (baseUrl) {
    try {
      const url = new URL(baseUrl);
      domain    = url.hostname;
      cmsDomain = `cms.${domain}`;
    } catch {}
  }

  return `# Generated by LCMS — do not edit manually.
# Update via CMS Settings (gear icon on the site selector page).

# ── Static generated sites ────────────────────────────────────────────────────
# Each site is served at https://${domain}/<site-slug>/
server {
    listen 80;
    server_name ${domain};

    root ${ROOT}/output;

    location / {
        try_files $uri $uri/ =404;
    }

    location ~ /\\. {
        deny all;
    }
}

# ── CMS application ───────────────────────────────────────────────────────────
server {
    listen 80;
    server_name ${cmsDomain};

    client_max_body_size 64m;

    location / {
        proxy_pass         http://127.0.0.1:3001;
        proxy_http_version 1.1;
        proxy_set_header   Host              $host;
        proxy_set_header   X-Real-IP         $remote_addr;
        proxy_set_header   X-Forwarded-For   $proxy_add_x_forwarded_for;
        proxy_set_header   X-Forwarded-Proto $scheme;
        proxy_request_buffering off;
    }
}
`;
}

router.get('/', (req, res) => {
  res.json(read());
});

router.put('/', (req, res) => {
  try {
    const existing = read();
    const baseUrl = typeof req.body.baseUrl === 'string'
      ? req.body.baseUrl.trim().replace(/\/+$/, '').slice(0, 500)
      : existing.baseUrl;
    const updated = { ...existing, baseUrl };
    fs.writeFileSync(CMS_SETTINGS_FILE, JSON.stringify(updated, null, 2));
    fs.writeFileSync(NGINX_CONF_FILE, buildNginxConf(updated));
    res.json(updated);
  } catch (err) { res.status(500).json({ error: safeError(err) }); }
});

module.exports = router;
