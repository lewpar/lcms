'use strict';

const fs     = require('fs');
const router = require('express').Router();
const { CMS_SETTINGS_FILE, NGINX_CONF_FILE, OUTPUT_DIR } = require('../lib/paths');
const { safeError } = require('../lib/validate');

const DEFAULTS = { baseUrl: '', outputPath: OUTPUT_DIR };

function read() {
  try { return { ...DEFAULTS, ...JSON.parse(fs.readFileSync(CMS_SETTINGS_FILE, 'utf-8')) }; }
  catch { return { ...DEFAULTS }; }
}

function buildNginxConf(settings) {
  const baseUrl    = settings.baseUrl || '';
  const outputPath = settings.outputPath || OUTPUT_DIR;
  let domain       = 'your-domain.com';

  if (baseUrl) {
    try { domain = new URL(baseUrl).hostname; } catch {}
  }

  return `# Generated by LCMS — do not edit manually.
# Update via CMS Settings (gear icon on the site selector page).

# Each site is served at https://${domain}/<site-slug>/
server {
    listen 80;
    server_name ${domain};

    root ${outputPath};

    location / {
        try_files $uri $uri/ =404;
    }

    location ~ /\\. {
        deny all;
    }
}
`;
}

router.get('/', (req, res) => {
  res.json(read());
});

router.put('/', (req, res) => {
  try {
    const existing = read();
    const baseUrl = typeof req.body.baseUrl === 'string'
      ? req.body.baseUrl.trim().replace(/\/+$/, '').slice(0, 500)
      : existing.baseUrl;
    const outputPath = typeof req.body.outputPath === 'string'
      ? req.body.outputPath.trim().slice(0, 500) || OUTPUT_DIR
      : existing.outputPath;
    const updated = { ...existing, baseUrl, outputPath };
    fs.writeFileSync(CMS_SETTINGS_FILE, JSON.stringify(updated, null, 2));
    fs.writeFileSync(NGINX_CONF_FILE, buildNginxConf(updated));
    res.json(updated);
  } catch (err) { res.status(500).json({ error: safeError(err) }); }
});

module.exports = router;
